class Tune {
    /**
     * Tune data
     * 
     * @param { String } name - Name of this tune.
     * @param { String } data - Sound data.
     * @param { String } id -  Tune id.
     */
    constructor(name, data, id) {
        this.name = name;
        this.data = data;
        this.id = id;
    }
}